# Day 5 - Collections

# Lists

fruits = ["Apple", "Banana", "Mango", "Orange"]

print(fruits)
print(fruits[0])

fruits.append("Pineapple")

print(fruits)

# Tuples

student = ("Ashutosh", 21, "Ahmedabad")

print(student)

# Sets

numbers = {10, 20, 30, 40, 10, 20}

print(numbers)

# Dictionaries

student_info = {
    "name": "Ashutosh",
    "age": 21,
    "course": "Data Analytics"
}

print(student_info)

print(student_info["name"])
print(student_info["course"])